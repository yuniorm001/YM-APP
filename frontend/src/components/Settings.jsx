const getFriendlyEmailError = (error) => {
  const raw = String(error?.message || error || '').trim();
  const lower = raw.toLowerCase();

  if (
    lower.includes('not a valid email') ||
    lower.includes('valid email address') ||
    lower.includes('@-sign') ||
    lower.includes('email address') ||
    lower.includes('invalid email')
  ) {
    return 'Escribe un correo válido. Ejemplo: cliente@dominio.com';
  }

  if (
    lower.includes('ya estaba agregado') ||
    lower.includes('ya está permitido') ||
    lower.includes('already') ||
    lower.includes('duplicate') ||
    lower.includes('exists')
  ) {
    return 'Ese correo ya existe en los accesos autorizados.';
  }

  return raw || 'No pude agregar el correo. Revisa la información e intenta otra vez.';
};

