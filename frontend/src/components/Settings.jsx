const getNextCycleDate = (dateValue, frequency = 'monthly') => {
  const next = new Date(dateValue);
  next.setHours(12, 0, 0, 0);
  if (frequency === 'weekly') {
    next.setDate(next.getDate() + 7);
  } else if (frequency === 'biweekly') {
    next.setDate(next.getDate() + 14);
  } else {
    next.setMonth(next.getMonth() + 1);
  }
  return next;
};

const getLatestDueCycleDate = (anchorDate, frequency, currentDate) => {
  const current = new Date(currentDate);
  current.setHours(23, 59, 59, 999);
  const cursor = new Date(anchorDate);
  cursor.setHours(12, 0, 0, 0);

  if (Number.isNaN(cursor.getTime()) || cursor > current) return null;

  let latest = new Date(cursor);
  while (true) {
    const next = getNextCycleDate(latest, frequency);
    if (next > current) break;
    latest.setTime(next.getTime());
  }
  return latest;
};

const getCycleKey = (dateValue) => {
  const date = new Date(dateValue);
  date.setHours(12, 0, 0, 0);
  return date.toISOString().split('T')[0];
};

const getPendingVariableCycleInfo = (primaryEntry, entries = [], currentDate) => {
  if (!primaryEntry || (primaryEntry.incomeType || 'fixed') !== 'variable') return null;

  const latestDueDate = getLatestDueCycleDate(primaryEntry.anchorDate || primaryEntry.date || currentDate, primaryEntry.frequency || 'monthly', currentDate);
  if (!latestDueDate) return null;

  const cycleKey = getCycleKey(latestDueDate);
  const matchingEntry = (entries || []).find((entry) => entry.type === 'primary-variable-cycle' && entry.cycleAnchorDate === cycleKey);
  const nextCycleDate = getNextCycleDate(latestDueDate, primaryEntry.frequency || 'monthly');

  return {
    cycleKey,
    dueDate: latestDueDate,
    nextCycleDate,
    isPending: !matchingEntry,
    existingEntry: matchingEntry || null
  };
};

